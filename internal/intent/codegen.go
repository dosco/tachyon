const generatedDir = "internal/intent/generated/current"

func Generate(bundle Bundle, dir string) error {
	ir, err := Check(bundle)
	if err != nil {
		return err
	}
	if dir == "" {
		dir = generatedDir
	}
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	files := map[string][]byte{}
	reg, err := format.Source([]byte(generateRegistry(ir)))
	if err != nil {
		return fmt.Errorf("format registry: %w", err)
	}
	files["registry_gen.go"] = reg
	testSrc, err := format.Source([]byte(generateRegistryTest(ir)))
	if err != nil {
		return fmt.Errorf("format registry test: %w", err)
	}
	files["registry_gen_test.go"] = testSrc
	benchSrc, err := format.Source([]byte(generateRegistryBench(ir)))
	if err != nil {
		return fmt.Errorf("format registry bench: %w", err)
	}
	files["registry_gen_bench_test.go"] = benchSrc
	configSrc, err := format.Source([]byte(generateConfig(ir)))
	if err != nil {
		return fmt.Errorf("format config: %w", err)
	}
	files["config_gen.go"] = configSrc
	configTestSrc, err := format.Source([]byte(generateConfigTest(ir)))
	if err != nil {
		return fmt.Errorf("format config test: %w", err)
	}
	files["config_gen_test.go"] = configTestSrc
	for name, src := range files {
		if err := os.WriteFile(filepath.Join(dir, name), src, 0o644); err != nil {
			return err
		}
	}
	return nil
}

func generateRegistry(bundle IRBundle) string {
	var b bytes.Buffer
	b.WriteString("// Code generated by tachyon intent build. DO NOT EDIT.\n\n")
	b.WriteString("package current\n\n")
	b.WriteString("import (\n")
	b.WriteString("\t\"tachyon/internal/intent/runtime\"\n")
	b.WriteString("\t\"tachyon/internal/router\"\n")
	b.WriteString(")\n\n")
	b.WriteString("var Registry = runtime.Registry{\n")
	b.WriteString(fmt.Sprintf("\tVersion: %q,\n", bundle.Version))
	b.WriteString("\tPolicies: map[string]runtime.PolicyMeta{\n")
	for _, p := range bundle.Policies {
		b.WriteString(fmt.Sprintf("\t\t%q: {\n", p.Name))
		b.WriteString(fmt.Sprintf("\t\t\tName: %q,\n", p.Name))
		b.WriteString(fmt.Sprintf("\t\t\tPriority: %d,\n", p.Priority))
		if len(p.Match) > 0 {
			b.WriteString("\t\t\tMatch: []runtime.MatchCondition{\n")
			for _, cond := range p.Match {
				b.WriteString("\t\t\t\t{")
				b.WriteString(fmt.Sprintf("Field: runtime.MatchField(%q), ", string(cond.Field)))
				if cond.Name != "" {
					b.WriteString(fmt.Sprintf("Name: %q, ", cond.Name))
				}
				b.WriteString(fmt.Sprintf("Value: %q", cond.Value))
				b.WriteString("},\n")
			}
			b.WriteString("\t\t\t},\n")
		}
		if len(p.Request) > 0 {
			b.WriteString("\t\t\tRequest: []runtime.Action{\n")
			for _, act := range p.Request {
				b.WriteString(formatActionLiteral(act))
			}
			b.WriteString("\t\t\t},\n")
		}
		if len(p.Response) > 0 {
			b.WriteString("\t\t\tResponse: []runtime.Action{\n")
			for _, act := range p.Response {
				b.WriteString(formatActionLiteral(act))
			}
			b.WriteString("\t\t\t},\n")
		}
		if len(p.Error) > 0 {
			b.WriteString("\t\t\tError: []runtime.Action{\n")
			for _, act := range p.Error {
				b.WriteString(formatActionLiteral(act))
			}
			b.WriteString("\t\t\t},\n")
		}
		b.WriteString("\t\t\tPrimitives: []string{")
		for i, primitive := range p.Primitives {
			if i > 0 {
				b.WriteString(", ")
			}
			b.WriteString(fmt.Sprintf("%q", primitive))
		}
		b.WriteString("},\n")
		if p.RequiresClassC {
			b.WriteString("\t\t\tRequiresClassC: true,\n")
		}
		b.WriteString("\t\t},\n")
	}
	b.WriteString("\t},\n")
	b.WriteString("}\n\n")
	b.WriteString("func BuildRoutePrograms(routes []router.Rule) (runtime.RoutePrograms, error) {\n")
	b.WriteString("\treturn runtime.BindRoutes(routes, Registry)\n")
	b.WriteString("}\n")
	return b.String()
}

func formatActionLiteral(act rt.Action) string {
	return fmt.Sprintf("\t\t\t\t{Kind: runtime.ActionKind(%q), Str1: %q, Str2: %q, Int1: %d, Int2: %d},\n",
		string(act.Kind), act.Str1, act.Str2, act.Int1, act.Int2)
}

