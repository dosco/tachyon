func generateRegistryBench(bundle IRBundle) string {
	allNames := make([]string, 0, len(bundle.Policies))
	for _, p := range bundle.Policies {
		allNames = append(allNames, p.Name)
	}
	sort.Strings(allNames)

	requestHot := firstPolicy(bundle.Policies, func(p IRPolicy) bool {
		return len(p.Request) > 0 && !hasPrimitive(p, string(rt.ActionRateLimitLocal)) && !p.RequiresClassC
	})
	rateLimit := firstPolicy(bundle.Policies, func(p IRPolicy) bool {
		return hasPrimitive(p, string(rt.ActionRateLimitLocal))
	})
	responseHeaders := firstPolicy(bundle.Policies, func(p IRPolicy) bool {
		return len(p.Response) > 0
	})
	externalAuth := firstPolicy(bundle.Policies, func(p IRPolicy) bool {
		return hasPrimitive(p, string(rt.ActionAuthExternal))
	})

	var b bytes.Buffer
	b.WriteString("package current\n\n")
	b.WriteString("import (\n")
	b.WriteString("\t\"net/http\"\n")
	b.WriteString("\t\"testing\"\n")
	if externalAuth != nil {
		b.WriteString("\t\"io\"\n")
		b.WriteString("\t\"strings\"\n")
	}
	if rateLimit != nil {
		b.WriteString("\t\"time\"\n")
	}
	b.WriteString("\n")
	b.WriteString("\t\"tachyon/internal/intent/runtime\"\n")
	b.WriteString("\t\"tachyon/internal/router\"\n")
	b.WriteString(")\n\n")
	b.WriteString("type benchRoundTripper func(*http.Request) (*http.Response, error)\n\n")
	b.WriteString("func (fn benchRoundTripper) RoundTrip(req *http.Request) (*http.Response, error) {\n")
	b.WriteString("\treturn fn(req)\n")
	b.WriteString("}\n\n")
	b.WriteString("func benchmarkRouteSet(names ...string) runtime.RoutePolicySet {\n")
	b.WriteString("\trp, err := BuildRoutePrograms([]router.Rule{{RouteID: 1, Upstream: \"origin\", Intents: names}})\n")
	b.WriteString("\tif err != nil {\n")
	b.WriteString("\t\tpanic(err)\n")
	b.WriteString("\t}\n")
	b.WriteString("\treturn rp.ByRouteID[1]\n")
	b.WriteString("}\n\n")
	b.WriteString("func BenchmarkNoMatch(b *testing.B) {\n")
	b.WriteString("\tb.ReportAllocs()\n")
	b.WriteString(fmt.Sprintf("\tset := benchmarkRouteSet(%s)\n", quotedArgs(allNames)))
	b.WriteString("\tstate := runtime.NewState()\n")
	b.WriteString("\treq := runtime.StaticRequest{MethodValue: http.MethodGet, HostValue: \"bench.invalid\", PathValue: \"/miss\"}\n")
	b.WriteString("\tfor i := 0; i < b.N; i++ {\n")
	b.WriteString("\t\t_ = runtime.ExecuteRequest(set, state, req, \"origin\")\n")
	b.WriteString("\t}\n")
	b.WriteString("}\n\n")

	b.WriteString("func BenchmarkRequestHotPath(b *testing.B) {\n")
	b.WriteString("\tb.ReportAllocs()\n")
	if requestHot == nil {
		b.WriteString("\tb.Skip(\"no request-phase non-rate-limit policy in generated bundle\")\n")
		b.WriteString("}\n\n")
	} else {
		b.WriteString(fmt.Sprintf("\tset := benchmarkRouteSet(%q)\n", requestHot.Name))
		b.WriteString("\tstate := runtime.NewState()\n")
		b.WriteString("\treq := runtime.StaticRequest{MethodValue: http.MethodGet, HostValue: \"example.com\", PathValue: \"/hot\"}\n")
		b.WriteString("\tfor i := 0; i < b.N; i++ {\n")
		b.WriteString("\t\t_ = runtime.ExecuteRequest(set, state, req, \"origin\")\n")
		b.WriteString("\t}\n")
		b.WriteString("}\n\n")
	}

	b.WriteString("func BenchmarkRequestRateLimitHotKey(b *testing.B) {\n")
	b.WriteString("\tb.ReportAllocs()\n")
	if rateLimit == nil {
		b.WriteString("\tb.Skip(\"no local rate limit policy in generated bundle\")\n")
		b.WriteString("}\n\n")
	} else {
		b.WriteString(fmt.Sprintf("\tset := benchmarkRouteSet(%q)\n", rateLimit.Name))
		b.WriteString("\tstate := runtime.NewState()\n")
		b.WriteString("\treq := runtime.StaticRequest{MethodValue: http.MethodGet, HostValue: \"example.com\", PathValue: \"/limited\", HeadersValue: map[string]string{\"x-api-key\": \"bench-hot-key\"}, ClientIPValue: \"127.0.0.1\"}\n")
		b.WriteString("\tfor i := 0; i < b.N; i++ {\n")
		b.WriteString("\t\tstate.SetNowTime(time.Unix(int64(i), 0).UTC())\n")
		b.WriteString("\t\t_ = runtime.ExecuteRequest(set, state, req, \"origin\")\n")
		b.WriteString("\t}\n")
		b.WriteString("}\n\n")
	}

	b.WriteString("func BenchmarkResponseHeaders(b *testing.B) {\n")
	b.WriteString("\tb.ReportAllocs()\n")
	if responseHeaders == nil {
		b.WriteString("\tb.Skip(\"no response-phase policy in generated bundle\")\n")
		b.WriteString("}\n\n")
	} else {
		b.WriteString(fmt.Sprintf("\tset := benchmarkRouteSet(%q)\n", responseHeaders.Name))
		b.WriteString("\tfor i := 0; i < b.N; i++ {\n")
		b.WriteString("\t\t_ = runtime.ExecuteResponse(set, func(string) string { return \"\" })\n")
		b.WriteString("\t}\n")
		b.WriteString("}\n\n")
	}

	b.WriteString("func BenchmarkExternalAuthFastFail(b *testing.B) {\n")
	b.WriteString("\tb.ReportAllocs()\n")
	if externalAuth == nil {
		b.WriteString("\tb.Skip(\"no external auth policy in generated bundle\")\n")
	} else {
		b.WriteString(fmt.Sprintf("\tset := benchmarkRouteSet(%q)\n", externalAuth.Name))
		b.WriteString("\tstate := runtime.NewState()\n")
		b.WriteString("\tstate.SetHTTPClient(&http.Client{Transport: benchRoundTripper(func(*http.Request) (*http.Response, error) {\n")
		b.WriteString("\t\treturn &http.Response{StatusCode: http.StatusForbidden, Body: io.NopCloser(strings.NewReader(\"deny\")), Header: make(http.Header)}, nil\n")
		b.WriteString("\t})})\n")
		b.WriteString("\treq := runtime.StaticRequest{MethodValue: http.MethodGet, HostValue: \"example.com\", PathValue: \"/auth\"}\n")
		b.WriteString("\tfor i := 0; i < b.N; i++ {\n")
		b.WriteString("\t\t_ = runtime.ExecuteRequest(set, state, req, \"origin\")\n")
		b.WriteString("\t}\n")
	}
	b.WriteString("}\n")
	return b.String()
}

func quotedArgs(values []string) string {
	if len(values) == 0 {
		return ""
	}
	parts := make([]string, 0, len(values))
	for _, v := range values {
		parts = append(parts, fmt.Sprintf("%q", v))
	}
	return strings.Join(parts, ", ")
}

func firstPolicy(policies []IRPolicy, fn func(IRPolicy) bool) *IRPolicy {
	for i := range policies {
		if fn(policies[i]) {
			return &policies[i]
		}
	}
	return nil
}

func hasPrimitive(policy IRPolicy, primitive string) bool {
	for _, p := range policy.Primitives {
		if p == primitive {
			return true
		}
	}
	return false
}

func policyNeedsState(policy IRPolicy) bool {
	for _, primitive := range policy.Primitives {
		switch primitive {
		case string(rt.ActionRateLimitLocal), string(rt.ActionAuthExternal), string(rt.ActionCanary):
			return true
		}
	}
	return false
}

func sortedMapPairs(values map[string]string) [][2]string {
	if len(values) == 0 {
		return nil
	}
	keys := make([]string, 0, len(values))
	for k := range values {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	out := make([][2]string, 0, len(keys))
	for _, k := range keys {
		out = append(out, [2]string{k, values[k]})
	}
	return out
}

func defaultString(v, fallback string) string {
	if v == "" {
		return fallback
	}
	return v
}

